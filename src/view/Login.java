    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        App.startData();
        String usuario = LoginField.getText();
        String senha = SenhaField.getText();

        Funcionarios func = DadosFuncionarios.buscarLogin(usuario);
        if (func == null) {
            JOptionPane.showMessageDialog(null, "Usuario NÃ£o encontrado.");
        } else {
            if (func.getSenha() == null ? senha == null : func.getSenha().equals(senha)) {
                if (func instanceof Medicos) {
                    
                            
                    MenuMedico menumedico = new MenuMedico(func.getCpf());
                    menumedico.setVisible(true);
                } else {
                    Menu menu = new Menu(func.getCpf());
                    menu.setVisible(true);
                }
            }

            dispose();
        }
    }//GEN-LAST:event_CadastrarActionPerformed
    
    
    
