    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        App.startData();
        String nome = NomeField.getText();
        String comPlano = ComPlano.getText();
        String semPlano = SemPlano.getText();
        if (nome.equals("")) {

            NomeField.setBackground(Color.red);
            NomeField.setForeground(Color.white);
            JOptionPane.showMessageDialog(null, "Digite o Nome Do exame .");
            NomeField.requestFocus();
        } else {
            NomeField.setBackground(Color.white);
            NomeField.setForeground(Color.black);
            if ("".equals(SemPlano.getText())) {
                JOptionPane.showMessageDialog(null, "Digite o valor Do exame SEM plano.");
            } else if ("".equals(ComPlano.getText())) {
                JOptionPane.showMessageDialog(null, "Digite o valor Do exame COM plano.");
            } else {
                if (!DadosExames.cadastrar(new Exames(nome, Double.parseDouble(semPlano), Double.parseDouble(comPlano)))) {
                    JOptionPane.showMessageDialog(null, "Exame já cadastrado");
                } else {

                    JOptionPane.showMessageDialog(null, "Cadastro com sucesso");
                    App.saveData();
                }
            }
        }

        NomeField.setText(null);
        ComPlano.setText(null);
        SemPlano.setText(null);

    }//GEN-LAST:event_CadastrarActionPerformed

