    private void PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarActionPerformed
        DadosExames.inicializaExames();
        String nome = NomeField.getText();
        Exames exam = DadosExames.buscar(nome);

        if (exam == null) {
            JOptionPane.showMessageDialog(null, "Exame Não encontrado.");
        } else {
            JOptionPane.showMessageDialog(null, "Nome: " + exam.getNome() + " Valor sem plano: " + exam.getSemPlano() + " Valor com plano:" + exam.getComPlano());
        }

        DadosExames.gravar();
    }//GEN-LAST:event_PesquisarActionPerformed

    private void DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarActionPerformed
        DadosExames.inicializaExames();
        String nome = NomeField.getText();
        Exames exam = DadosExames.buscar(nome);
        if (exam == null) {
            JOptionPane.showMessageDialog(null, "Exame Não encontrado.");
        } else {
            DadosExames.excluir(nome);

            JOptionPane.showMessageDialog(null, "Deletado com sucesso.");
        }

        DadosExames.gravar();
    }//GEN-LAST:event_DeletarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DadosExames.inicializaExames();

        ArrayList<Exames> examesArrayList = DadosExames.devolve();
        StringBuilder stringfinal = new StringBuilder();

        for (Exames objeto : examesArrayList) {
            stringfinal.append("Nome do exame: [");
            stringfinal.append(objeto.getNome());
            stringfinal.append("] \t Valor Com plano: ");
            stringfinal.append(objeto.getComPlano());
            stringfinal.append(" \t Valor Sem plano: ");
            stringfinal.append(objeto.getSemPlano());
            stringfinal.append("\n\n");
        }
        JOptionPane.showMessageDialog(null, stringfinal);
        DadosExames.gravar();
    }//GEN-LAST:event_jButton1ActionPerformed

